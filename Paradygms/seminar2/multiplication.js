// процедурное программирование лучше подходит для реализации таблицы умножения, так как функция позволяет переиспользовать код
function multiplicationTable(n) {
    // Цикл для создания строк таблицы
    for (let i = 1; i <= n; i++) {
        let row = '';

        // Цикл для создания ячеек в строке
        for (let j = 1; j <= n; j++) {
            // Вычисляем значение ячейки - произведение чисел
            let value = i * j;

            // Форматируем строку ячейки
            row += `${j} * ${i} = ${value}\t`;
        }

        // Выводим строку в консоль
        console.log(row);
    }
}

// Пример вызова функции с n = 10
multiplicationTable(10);
